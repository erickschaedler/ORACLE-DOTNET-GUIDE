CREATE TABLE CORES (
    ID_COR NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NOME_COR VARCHAR2(50) NOT NULL,
    CONSTRAINT PK_CORES PRIMARY KEY (ID_COR)
);

CREATE TABLE POKEMONS (
    ID_POKEMON NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NOME_POKEMON VARCHAR2(100) NOT NULL,
    ID_COR NUMBER NOT NULL,
    CONSTRAINT PK_POKEMONS PRIMARY KEY (ID_POKEMON),
    CONSTRAINT FK_POKEMONS_CORES FOREIGN KEY (ID_COR) REFERENCES CORES(ID_COR)
);